import React from 'react';

export default function Result({ percentage, onRestart }) {
  const getResult = (percentage) => {
    if (percentage <= 20) {
      return {
        title: "Низкая вероятность ОКР",
        description: "Ваши результаты показывают минимальные признаки ОКР. У вас могут быть редкие навязчивые мысли или привычки, что является нормой для большинства людей. Если симптомы начнут усиливаться, стоит обратить на это внимание."
      };
    } else if (percentage <= 35) {
      return {
        title: "Легкие признаки",
        description: "Вы демонстрируете некоторые признаки, характерные для ОКР, но они минимально влияют на вашу жизнь. Это может быть проявлением тревожности или обычных привычек. Следите за своим состоянием, и если симптомы усилятся, подумайте о консультации специалиста."
      };
    } else if (percentage <= 50) {
      return {
        title: "Умеренные признаки",
        description: "У вас наблюдаются заметные признаки, характерные для ОКР, которые могут влиять на повседневную жизнь. Рекомендуется проконсультироваться с психологом или психотерапевтом для оценки состояния и возможной помощи."
      };
    } else if (percentage <= 70) {
      return {
        title: "Выраженные признаки",
        description: "Ваши результаты указывают на значительные признаки ОКР, которые существенно влияют на качество жизни. Настоятельно рекомендуется обратиться к специалисту (психотерапевту или психиатру) для профессиональной диагностики и помощи. ОКР успешно лечится."
      };
    } else {
      return {
        title: "Тяжелые признаки",
        description: "Ваши результаты показывают очень выраженные признаки ОКР, которые серьезно нарушают повседневную жизнь. Необходима срочная консультация специалиста. Помните: ОКР эффективно лечится с помощью когнитивно-поведенческой терапии (КПТ) и, при необходимости, медикаментозного лечения. Вы не одиноки, и помощь доступна."
      };
    }
  };

  const result = getResult(percentage);

  return (
    <div className="result-container">
      <div className="result-score">{percentage}%</div>
      <div className="result-title">{result.title}</div>
      <div className="result-description">{result.description}</div>

      <div className="disclaimer">
        <p className="disclaimer-text">
          <strong>Важно:</strong> Этот результат является ориентировочным и НЕ является диагнозом.
          Для точной диагностики необходима консультация квалифицированного специалиста.
          ОКР успешно лечится с помощью когнитивно-поведенческой терапии (КПТ) и, при необходимости, медикаментозного лечения.
        </p>
      </div>

      <button className="btn btn-primary btn-large" onClick={onRestart} style={{ marginTop: '20px' }}>
        Пройти тест заново
      </button>
    </div>
  );
}
